
# int() with an integer value
print("int(123) is:", int(123)) #Output: 123

# int() with a floating point value
print("int(123.23) is:", int(123.23)) #Output: 123

# int() with a numeric-string value
print("int('123') is:", int("123")) #Output: 123


# converting a binary to integer with int() base 2
print("For 0b101, int is:", int("0b101", 2))  #Output: 5

# converting a binary to integer with int()) base 8
print("For 0o16, int is:", int("0o16", 8))  #Output: 14

# converting a binary to integer with int() base 16
print("For 0xA, int is:", int("0xA", 16))  #Output: 10